#pragma once // 헤더 중복 포함 방지 지시어입니다.

#include "Transform.h" // 액터가 3D 공간에 존재하기 위해 반드시 필요한 트랜스폼 헤더를 가져옵니다.

//  [신규 파일] 게임 세상에 존재하는 모든 물체의 근간이 되는 Actor(액터) 클래스입니다. 
class Actor // Actor 클래스 선언입니다. (언리얼 엔진의 AActor와 같은 역할입니다)
{ // 클래스 내부 블록 시작입니다.
public: // 누구나 접근 가능한 퍼블릭 영역입니다.
    Actor(); // 액터가 태어날 때 호출되는 생성자입니다.
    virtual ~Actor(); // 다형성(자식 클래스의 정상적인 메모리 해제)을 위해 반드시 소멸자를 가상(virtual)으로 만듭니다.

    // 매 프레임마다 액터 스스로 로직을 갱신하도록 지시하는 업데이트 함수입니다. (자식이 덮어쓸 수 있게 가상 함수로 만듭니다)
    virtual void Update(float deltaTime);

    // 외부(렌더러나 로직)에서 이 액터의 위치나 회전값을 조회/수정할 수 있도록 트랜스폼 객체의 주소를 반환합니다.
    Transform* GetTransform() { return &mTransform; }

protected: // 자식 클래스에게만 직접적인 접근을 허용하는 프로텍티드 영역입니다.
    Transform mTransform; // 이 액터가 3D 월드 상에서 어디에 어떻게 위치하는지 나타내는 전용 트랜스폼 인스턴스입니다.
}; // 클래스 블록 끝입니다.